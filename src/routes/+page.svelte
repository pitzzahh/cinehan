<script lang="ts">
	import { onMount } from 'svelte';
	import { store, formatRuntime } from '$lib';
	import { AspectRatio } from '$lib/components/ui/aspect-ratio';
	import { Skeleton } from '$lib/components/ui/skeleton';
	import { ExclamationTriangle, Play, Star } from 'radix-icons-svelte';
	import * as Alert from '$lib/components/ui/alert';
	import type { PageData } from './$types';
	import { Splide, SplideSlide } from '@splidejs/svelte-splide';
	import '@splidejs/svelte-splide/css';
	import Tv from '$lib/config/icons/tv.svelte';
	import Clock from '$lib/config/icons/clock.svelte';
	import { Button } from '$lib/components/ui/button';

	export let data: PageData;

	onMount(async () => ($store.trendingMovies = data.streamed.trendingMovies));
</script>

{#await $store.trendingMovies}
	<AspectRatio ratio={16 / 6} class="bg-muted">
		<Skeleton>
			<div class="relative h-[74%] w-full object-cover">
				<div class=" absolute bottom-0 left-0 mx-4 h-full w-full p-4 text-xl text-white">
					<Skeleton class="mt-2 w-10 text-lg font-extrabold md:text-3xl lg:text-3xl" />
					<Skeleton class="mt-4 flex items-center">
						<Tv class="mr-2 h-4 w-4 text-primary" />
						<Skeleton class="md:text-md text-sm">MOVIE</Skeleton>
						<Star class="mx-2 h-4 w-4 text-primary" />
						<Skeleton class="md:text-md w-4 text-sm" />
						<Clock class="mx-2 h-4 w-4 text-primary" />
						<Skeleton class="md:text-md w-4 text-sm" />
					</Skeleton>
					<Skeleton class="md:text-md text-wrap mt-2 line-clamp-2 w-16 overflow-hidden text-sm" />
					<div class="mt-4 flex space-x-2">
						<Skeleton class="transition-all hover:bg-theme hover:text-theme-foreground">
							<Play class="mr-1 h-4 w-4" />
						</Skeleton>
					</div>
				</div>
			</div>
		</Skeleton>
	</AspectRatio>
{:then trendingMovies}
	<AspectRatio ratio={16 / 6} class="bg-muted">
		<Splide
			aria-label="Trending Movies"
			options={{
				type: 'loop',
				autoplay: true,
				pagination: false,
				perPage: 1,
				arrows: false,
				focus: 'center',
				gap: '1rem'
			}}
		>
			{#each trendingMovies as trendingMovie}
				<SplideSlide>
					<div class="relative h-full md:h-[74%] w-full object-cover">
						<img
							class="h-full w-full rounded object-cover"
							src={trendingMovie.cover}
							alt={trendingMovie.title.toString()}
						/>
						<div class="absolute inset-0 bg-black opacity-60" />
						<div class="absolute bottom-0 left-0 mx-4 p-4 text-xl text-white">
							<h1 class="mt-2 text-lg font-extrabold md:text-3xl lg:text-3xl">
								{trendingMovie.title}
							</h1>
							<span class="mt-4 flex items-center">
								<Tv class="mr-2 h-5 w-5 text-primary" />
								<span class="md:text-lg text-sm">{trendingMovie.type}</span>
								<Star class="mx-2 h-5 w-5 text-theme" />
								<span class="md:text-lg text-sm">{trendingMovie?.rating?.toFixed(1)}</span>
								<Clock class="mx-2 h-5 w-5 text-primary" />
								<span class="md:text-lg text-sm"
									>{formatRuntime(Number(trendingMovie.duration))}</span
								>
							</span>
							<span class="md:text-lg text-sm italic">
								{(trendingMovie.tags && trendingMovie.tags[0]) || ''}
							</span>
							<span
								class="md:text-lg mt-2 line-clamp-2 overflow-hidden text-ellipsis text-sm"
								title={trendingMovie.description}>{trendingMovie.description}</span
							>
							<div class="mt-4 flex space-x-2">
								<a href="/movies/{trendingMovie.id}">
									<Button class="transition-all hover:bg-theme hover:text-theme-foreground">
										<Play class="mr-1 h-5 w-5" /> Watch
									</Button>
								</a>
								<a href="/movies/{trendingMovie.id}">
									<Button class="transition-all hover:bg-theme hover:text-theme-foreground">
										<Star class="mr-1 h-5 w-5" /> Rate
									</Button>
								</a>
							</div>
						</div>
					</div>
				</SplideSlide>
			{/each}
		</Splide>
	</AspectRatio>
{:catch err}
	<div class="container mt-4">
		<Alert.Root variant="destructive">
			<ExclamationTriangle class="h-4 w-4" />
			<Alert.Title>Error</Alert.Title>
			<Alert.Description>{err.message}</Alert.Description>
		</Alert.Root>
	</div>
{/await}
